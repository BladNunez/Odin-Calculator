//Add function 
function add(numOne,numTwo){
        let num = numOne + numTwo;

        return num
}

//Sub function 
function sub(numOne,numTwo){
        let num = numOne - numTwo;

        return num;
}

//Divide function
function divide(numOne,numTwo){
        if(numOne === 0 || numTwo === 0){
                console.log("Can not divide by zero!");
        }

        let num = numOne / numTwo;

        return num
}

function multiply(numOne,numTwo){
        let num = numOne * numTwo;

        return num;
}





